<?php
/*
    Variables are names that store data.

    In PHP Variables start with $ which is why the \$ Escape key is sometimes required.

    $HelloWorld = "Hello World!";

    Is a standard PHP string variable. The variable name is always on the left, the data is on the right, and they
    are joined with the assignment operator.

    The terms you need to know are:

    Variable name > Operator > Variable Value.

    Variable name holds the data contained within Variable value. The operator indicates what is name, and what is value.
*/
    $HelloWorld = "Hello World!";
    echo $HelloWorld;
?>